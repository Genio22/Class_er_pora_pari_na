    private Timer animationTimer;
    private int r = 0, g = 255, b = 0; // Start with green
    private int step = 5; // Color change per step

    private void startAnimation() {
        animationTimer = new Timer(100, e -> {
            if (g == 255 && r < 255 && b == 0) {
                r += step;  // Green â†’ Yellow
            } else if (r == 255 && g > 0 && b == 0) {
                g -= step;  // Yellow â†’ Red
            } else if (r == 255 && b < 255 && g == 0) {
                b += step;  // Red â†’ Magenta
            } else if (b == 255 && r > 0 && g == 0) {
                r -= step;  // Magenta â†’ Blue
            } else if (b == 255 && g < 255 && r == 0) {
                g += step;  // Blue â†’ Cyan
            } else if (g == 255 && b > 0 && r == 0) {
                b -= step;  // Cyan â†’ Green
            }

            jLabel1.setForeground(new Color(r, g, b)); // Apply new color
        });
        animationTimer.start();
    }

    private void updateProgressBar() {
        // Define Ramadan start and Eid date
        LocalDate startOfRamadan = LocalDate.of(2025, 3, 1);  // 1 March 2025
        LocalDate eidDate = LocalDate.of(2025, 4, 1);  // 1 April 2025

        // today's date 
        LocalDate today = LocalDate.now();

        // Calculate total days from Ramadan start to Eid from 1 Mar to 1 Apr in 2025
        long totalDays = ChronoUnit.DAYS.between(startOfRamadan, eidDate);


        // Calculate how many days have passed since Ramadan started
        long daysPassed = ChronoUnit.DAYS.between(startOfRamadan, today);

        // Clamp daysPassed between 0 and totalDays
        if (daysPassed < 0) {
            // days rest hobe
            daysPassed = 0;
        } else if (daysPassed > totalDays) {
            // Eid day shai:)
            daysPassed = totalDays;
        }

        long daysLeft = totalDays - daysPassed;

        // progress bar value
        progressBar.setMinimum(0);
        progressBar.setMaximum((int) totalDays);
        progressBar.setValue((int) daysPassed);

        // Display "X days left for Eid!"
        progressBar.setString(daysLeft + " days left for Eid!");
    }

